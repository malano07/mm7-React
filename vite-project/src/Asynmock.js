export const getUnProducto = (id) => {
    return new Promise((resolve) => {
      setTimeout(() => {
        const producto = misProductos.find(item => item.id === String(id)); // Si el id en tus productos es string
        resolve(producto);
      }, 2000);
    });
  };

export const ProductosPorCategoria = (idCategoria) => {
    return new Promise((resolve) => {
      setTimeout(() => {
        const productosFiltrados = misProductos.filter(item => item.categoria === Number(idCategoria));
        resolve(productosFiltrados);
      }, 2000);
    });
  };

